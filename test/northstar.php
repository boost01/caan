	<H1>Lost Dog Behaviour</H1>
	Dogs are more difficult to recover than lost cats because they travel farther and are picked up by rescuers who determine their fate.  There are six major factors that influence the distances that lost dogs travel: Temperament, Circumstances, Weather, Terrain, Appearance, and Population Density. 
	<H3>Temperament of the dog</H3>
	<LI>Gregarious dogs are friendly with everyone and more inclined to go directly up to the first person who calls them.  They will generally be found fairly close to home or will be picked up by someone close to the escape point.  They are often "adopted" by individuals who find them.
	<LI>Aloof dogs are wary of strangers and will initially avoid human contact until they overcome their fear or become hungry enough.  While these dogs can travel a great distance, aloof dogs eventually can be enticed with food and patience.  They are often recovered by an experienced rescue group volunteer and their wariness can be misinterpreted as "abused".
	<LI>Fearful / Xenophobic dogs are afraid of anything unfamiliar and are more inclined to travel farther and are at higher fisk of being hit by cars.  It may be necessary to use other dogs or baited humane traps to capture these dogs.
	Circumstances surrounding the disappearance
	<LI><B>Weather:</B>  A dog that escapes on a beautiful day will travel farther than one that escapes in a snow storm.
	<LI><B>Terrain:</B>  A dog that escapes in a residential area will not travel as far as a dog that escapes in a rural area.
	<LI><B>Appearance:</B>  Small dogs and breeds known to be friendly are much more likely to be picked up quickly by a rescuer.
	<LI><B>Population Density:</B>  When dogs escape into areas with high numbers of people, their chances of being found close to the escape point are increased.  

	<H3>Human Behaviours:</H3>
	<B>Guardian Behaviors:</B>  Guardians often behave in ways that inhibit their chances of recovering their lost pets.  Some develop a "wait and see" attitude and by the time they start looking, the first vital hours to locate their pet or a witness who saw their pet are gone. Others focus on wrong theories or quickly give up the search because they believe they will never see their pet again.
	<BR>
	<B>Rescuer Behaviours:</B>	People who find stray dogs with skittish temperaments often misinterpret the dog's behaviour as a sign of abuse.  Dogs found in rural areas are often assumed to be "dumped" and dogs found without collars are assumed to be "homeless". Therefore the rescuer works to place the dog instead of looking for its owner.  In addition, the first place where the owner of a lost dog will search for their dog - the local shelter - is typically the last place that someone who finds a loose dog will take it (due to the fear of euthanasia).
	
