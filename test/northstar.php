	<H1>Lost Cat Behaviour:
	<H2>Outdoor-Access Cats:</H2>
	Cats are territorial.  When an outdoor cat suddenly vanishes, it means something had happened to the cat to interrupt its normal behaviour of returning home.  The cat could be injured, trapped, chased from its territory, or transported by a human.  When this happens, the temperament of the cat will influence how it behaves. 
	<H2>Indoor-Only Cats:</H2>
	The territory for an indoor-only cat is the inside of the home.  When an indoor-only cat escapes outdoors, it is "displaced" into unfamiliar territory.  Usually they will look for the first place to hide in silence as protection from predators.  Using baited humane traps is highly effective for recovering displaced, panicked cats that are hiding.
	<H3>Temperaments that Influence Distance Traveled:</H3>
	<LI>Curious / gregarious cats get into trouble easily, run to greet strangers, and are not easily frightened.  When displaced, they may initially hide but then will likely TRAVEL.  Strategy for recovery should be florescent posters within at least a 5 block radius and a thorough search of possible hiding places near the escape point.  Do not assume the cat will come when you call.
	<LI>Care-less or aloof cats don't seem to care much about people.  When displaced they will likely initially hide but will eventually come back to the door, meow, or possibly travel.  Strategy for recovery should include thorough search of possible hiding places and baited humane traps.
	<LI>Cautious cats are generally shy and hide from strangers.  When displaced, they will hide in fear.  If not frightened from their hiding places they will typically return to the escape point or will meow when the owner comes to look for them. This behaviour is typical within the first 2 days or not until 7-10 days when they are hungry or thirsty.  Strategy would be to conduct a tightly focused search in  neighbours' yards and baited humane traps. 
	<LI>Fearful or xenophobic cats are afraid of everything that is unfamiliar.  When displaced, they bolt and hide in silence becoming almost immobilized with fear.  If found by someone other than the owner, they may be mistaken for a feral cat.  Strategy would be baited humane traps.
	<P>The primary technique to recover a missing cat should be to obtain permission from all neighbours to enter their yards and conduct an aggressive search for the missing cat and to set humane traps when necessary.  Simply asking a neighbour to look for the lost cat is not sufficient!  Neighbours are not going to crawl around on their bellies under their decks or houses to search for someone else's cat.
