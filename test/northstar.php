The recovery tips are unique.  They are 'species specific' and they are 'behavior specific'. The methods, techniques and resources that you use to search for  a lost cat are very different from those you should use to search for a lost dog or a lost ferret.  Cats, dogs and ferrets are very different animals.  They behave differently, they are responded to differently when people see them, and they travel different distances when lost.
<P>
Behavior specific means the individual behavior and temperament of a cat, dog, ferret or other species will influence how that particular animal will respond to human contact as well as the distance he/she will travel when lost. The roots of this knowledge in lost pet behavior stem from MPP's knowledge of studies conducted into the behavioral patterns of missing people.  There is a science to finding lost people and a lot of that knowledge can be applied to finding lost pets. Professionally trained searchers don't wander aimlessly in the woods when searching for a missing hiker. Instead, an organized search plan is implemented based on the knowledge of the behavioral patterns of lost people.
