<H1 align=center>About Us</H1>	
<P>Community Animal Allies of Niagara, we have no need to re-invent a wheel. We got together in December 2006 by asking whoever wanted to come to a meeting to promote the no kill movement in Niagara. We kept meeting and have become not for profit incorporated in May 2007.
<P>
We want to establish programs and services that will make Niagara the safest place for a lost or homeless pet or feral cat to be in Canada.  
<P>
We found an old 1969 trailer, bought it and transformed it into the Cat<b>MOBILE</b>.  This is our first project, an adoption outreach trailer, in the summer of 2007.
<P>
We want to encourage the people of Niagara to make Niagara a <A HREF="index.php?action=nokill"><b>NO KILL</b></A> community.

